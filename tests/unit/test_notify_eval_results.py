from __future__ import annotations

import json
from pathlib import Path


def _write_json(path: Path, payload: dict) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def test_load_stack_status_returns_available_when_summary_exists(tmp_path: Path) -> None:
    from scripts.notify_eval_results import _load_stack_status

    summary_path = tmp_path / "stack_summary.json"
    index_path = tmp_path / "index.json"
    _write_json(
        summary_path,
        {
            "status": "ok",
            "missing_count": 0,
            "stale_count": 1,
            "mismatch_count": 0,
            "static_report_html": "reports/eval_history/report_static/index.html",
            "interactive_report_html": "reports/eval_history/report_interactive/index.html",
        },
    )
    _write_json(
        index_path,
        {
            "landing_page_html": "reports/eval_history/index.html",
        },
    )

    status = _load_stack_status(str(summary_path), str(index_path))

    assert status["available"] is True
    assert status["status"] == "ok"
    assert status["stale_count"] == 1
    assert status["landing_page"] == "reports/eval_history/index.html"
    assert status["static_report"] == "reports/eval_history/report_static/index.html"
    assert status["interactive_report"] == "reports/eval_history/report_interactive/index.html"


