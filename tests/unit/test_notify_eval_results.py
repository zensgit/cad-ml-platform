def test_slack_message_includes_stack_status_field(tmp_path: Path) -> None:
    from scripts.notify_eval_results import NotificationFormatter, _load_stack_status

    summary_path = tmp_path / "stack_summary.json"
    _write_json(
        summary_path,
        {
            "status": "degraded",
            "missing_count": 1,
            "stale_count": 0,
            "mismatch_count": 0,
        },
    )

    stack_status = _load_stack_status(str(summary_path), "")

    formatter = NotificationFormatter()
    results = {
        "timestamp": "2026-03-30T12:00:00Z",
        "combined": {"combined_score": 0.81, "vision_score": 0.78, "ocr_score": 0.84},
        "branch": "main",
        "commit": "abc1234",
    }
    message = formatter.format_slack_message(results, [], {}, None)

    # Inject stack status as the script does
    if stack_status["available"] and message.get("attachments"):
        message["attachments"][0]["fields"].append({
            "title": "Eval Reporting Stack",
            "value": (
                f"status={stack_status['status']}, "
                f"missing={stack_status['missing_count']}, "
                f"stale={stack_status['stale_count']}, "
                f"mismatch={stack_status['mismatch_count']}"
            ),
            "short": False,
        })

    field_titles = [f["title"] for f in message["attachments"][0]["fields"]]
    assert "Eval Reporting Stack" in field_titles
    stack_field = next(f for f in message["attachments"][0]["fields"] if f["title"] == "Eval Reporting Stack")
    assert "status=degraded" in stack_field["value"]
    assert "missing=1" in stack_field["value"]


def test_github_issue_body_includes_stack_status(tmp_path: Path) -> None:
    from scripts.notify_eval_results import _load_stack_status

    summary_path = tmp_path / "stack_summary.json"
    _write_json(
        summary_path,
        {
            "status": "degraded",
            "missing_count": 2,
            "stale_count": 0,
            "mismatch_count": 1,
        },
    )

    stack_status = _load_stack_status(str(summary_path), "")

    # Simulate the GitHub issue body construction as the script does
    breaches = ["Combined score below threshold"]
    results = {
        "combined": {"combined_score": 0.70, "vision_score": 0.65, "ocr_score": 0.75},
        "branch": "main",
        "commit": "abc1234",
        "timestamp": "2026-03-30T12:00:00Z",
    }
    body = f"""
## Evaluation Threshold Breach Alert

The latest evaluation has breached configured thresholds:

{chr(10).join(f'- {b}' for b in breaches)}

### Current Scores
- Combined: {results['combined']['combined_score']:.3f}

*This issue was automatically generated by the evaluation notification system.*
"""
    if stack_status["available"]:
        body += (
            f"\n### Eval Reporting Stack\n"
            f"- Status: {stack_status['status']}\n"
            f"- Missing: {stack_status['missing_count']}\n"
            f"- Stale: {stack_status['stale_count']}\n"
            f"- Mismatch: {stack_status['mismatch_count']}\n"
        )

    assert "### Eval Reporting Stack" in body
    assert "Status: degraded" in body
    assert "Missing: 2" in body
    assert "Mismatch: 1" in body


